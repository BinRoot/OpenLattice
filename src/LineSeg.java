
public class LineSeg {
	int x1;
	int y1;
	int z1;
	
	int x2;
	int y2;
	int z2;
	
	public LineSeg(int x1,int y1,int z1,int x2,int y2,int z2) {
		this.x1 = x1;
		this.y1 = y1;
		this.z1 = z1;
		
		this.x2 = x2;
		this.y2 = y2;
		this.z2 = z2;
	}
	
	
	public void setCoor(int x1,int y1,int z1,int x2,int y2,int z2) {
		this.x1 = x1;
		this.y1 = y1;
		this.z1 = z1;
		
		this.x2 = x2;
		this.y2 = y2;
		this.z2 = z2;
	}
	
}
